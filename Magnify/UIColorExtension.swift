import UIKit

extension UIColor {
    class func tungstenColor() -> UIColor {
        return UIColor(red:0.15, green:0.15, blue:0.15, alpha:1)
    }
    class func greenColor() -> UIColor {
        return UIColor(red:0.18, green:0.71, blue:0.29, alpha:1)
    }
}
